// string variable
var str = "welcome";
var ch = "A"

// boolean variable
var a = true
var b = false

// null and undefined
var p = null
var q = undefined

console.log(`string str = `, str)
console.log(`type str = `, typeof str)
console.log(`\n`)

console.log(`char ch = `, ch)
console.log(`char ch = `, typeof ch)
console.log(`\n`)

console.log(`boolean a = `, a)
console.log(`boolean a = `, typeof a)
console.log(`\n`)

console.log(`boolean b = `, b)
console.log(`boolean b = `, typeof b)
console.log(`\n`)

console.log(`null p = `, p)    // object
console.log(`null p = `, typeof p)    // object
console.log(`\n`)

console.log(`undefined q = `, q)
console.log(`undefined q = `, typeof q)
console.log(`\n`)


